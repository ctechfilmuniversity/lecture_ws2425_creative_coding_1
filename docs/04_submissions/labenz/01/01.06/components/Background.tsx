const generateGridStyles = (count: number, gridSize: number) => {
  const positions = [];
  const cellWidth = 100 / gridSize; // Breite und Höhe der Zellen in Prozent
  const cellHeight = 100 / gridSize;

  for (let i = 0; i < count; i++) {
    const row = Math.floor(i / gridSize);
    const col = i % gridSize;

    // Berechnen der Startpositionen jeder Zelle
    const baseTop = row * cellHeight;
    const baseLeft = col * cellWidth;

    // Hinzufügen eines kleinen zufälligen Versatzes
    const offsetTop = Math.random() * (cellHeight / 2) - cellHeight / 4;
    const offsetLeft = Math.random() * (cellWidth / 2) - cellWidth / 4;

    positions.push({
      top: `calc(${baseTop}% + ${offsetTop}vh)`,
      left: `calc(${baseLeft}% + ${offsetLeft}vw)`,
      transform: `scale(${Math.random() * 1.5 + 0.5}) rotate(${
        Math.random() * 360
      }deg)`,
    });
  }

  return positions;
};

